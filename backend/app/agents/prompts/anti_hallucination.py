MEDCOMM_ANTI_HALLUCINATION = load_layer1_anti_hallucination() or _DEFAULT_ANTI_HALLUCINATION
VISUAL_ANTI_HALLUCINATION = load_layer1_visual_anti() or _DEFAULT_VISUAL_ANTI
SCRIPT_ANTI_HALLUCINATION = load_layer1_script_anti() or _DEFAULT_SCRIPT_ANTI


def get_format_specific_rules(content_format: str) -> str:
    """根据内容形式返回形式族专属防编造规则，非视觉/脚本类返回空字符串"""
    if not content_format:
        return ""
    if content_format in VISUAL_FORMATS:
        return VISUAL_ANTI_HALLUCINATION
    if content_format in SCRIPT_FORMATS:
        return SCRIPT_ANTI_HALLUCINATION
    return ""
