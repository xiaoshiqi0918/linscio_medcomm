HANDBOOK_ANTI_HALLUCINATION = _DEFAULT_HANDBOOK_RULES


def get_format_specific_rules(content_format: str) -> str:
    """根据内容形式返回形式族专属规则"""
    if not content_format:
        return ""
    if content_format in COMIC_FORMATS:
        return COMIC_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in CARD_FORMATS:
        return CARD_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in POSTER_FORMATS:
        return POSTER_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in PICTURE_BOOK_FORMATS:
        return PICTURE_BOOK_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in LONG_IMAGE_FORMATS:
        return LONG_IMAGE_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in ORAL_SCRIPT_FORMATS:
        return ORAL_SCRIPT_ANTI_HALLUCINATION + "\n" + SCRIPT_ANTI_HALLUCINATION
    if content_format in DRAMA_SCRIPT_FORMATS:
        return DRAMA_SCRIPT_ANTI_HALLUCINATION + "\n" + SCRIPT_ANTI_HALLUCINATION
    if content_format in STORYBOARD_FORMATS:
        return STORYBOARD_ANTI_HALLUCINATION + "\n" + VISUAL_ANTI_HALLUCINATION
    if content_format in HANDBOOK_FORMATS:
        return HANDBOOK_ANTI_HALLUCINATION
    if content_format in VISUAL_FORMATS:
        return VISUAL_ANTI_HALLUCINATION
    if content_format in SCRIPT_FORMATS:
        return SCRIPT_ANTI_HALLUCINATION
    if content_format in STORY_FORMATS:
        return STORY_ANTI_HALLUCINATION
    if content_format in DEBUNK_FORMATS:
        return DEBUNK_ANTI_HALLUCINATION
    if content_format in QA_FORMATS:
        return QA_ANTI_HALLUCINATION
    if content_format in RESEARCH_READ_FORMATS:
        return RESEARCH_READ_ANTI_HALLUCINATION
    return ""
